# Dictionary {}
# AKA --> Hashes or object in JavaScript
## Works exactly like a dictionary
## Has key and value pairs: Key = word, Value = meaning of word
### Dict = {'Key' : 'Value'}
# Syntax
my_dictionary = {'Tissues': 'Disposable piece of absorbent paper. used especially as a handkerchief or for cleaning the skin',
                 'Baseball Bat': 'Bat made of wood to play baseball'}
# Accessing or Re-assigning is with the key inside the []
my_dictionary['Baseball Bat'] = 'Really, you are looking up two seperate words'
print(my_dictionary['Baseball Bat'])

